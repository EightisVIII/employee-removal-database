    private void deleteEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteEmpActionPerformed
        // TODO add your handling code here:
        var name = JOptionPane.showInputDialog("Input the employee's name.");
        
        String sql = "DELETE FROM removedemployees WHERE LOWER(Name) = LOWER(?)";
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/removed_employees_database", "root", "");
            pst = con.prepareStatement(sql);
            pst.setString(1, name);
            int rowsAffected = pst.executeUpdate();
            if(rowsAffected > 0){
                JOptionPane.showMessageDialog(null, "Employee " + name + " has been removed from the database.");
            } else{
                JOptionPane.showMessageDialog(null, "No employee with the name " + name + " exists in our records.");
            }
            con.close();
        } catch(ClassNotFoundException | SQLException e){
            JOptionPane.showMessageDialog(null, "An error has occurred. Please try again.");
        }
    }//GEN-LAST:event_deleteEmpActionPerformed

