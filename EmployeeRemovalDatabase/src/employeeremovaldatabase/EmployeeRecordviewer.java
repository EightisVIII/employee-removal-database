    private void loadData() throws SQLException{
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/removed_employees_database", "root", "");
            pst = con.prepareStatement("SELECT * FROM removedemployees");
            ResultSet rs = pst.executeQuery();
            
            DefaultTableModel model = (DefaultTableModel) empDB.getModel();
                
            model.setRowCount(0);
                
            while(rs.next()){
                Object[] row = {
                    rs.getString("Name"),
                    rs.getString("Department"),
                    rs.getString("Date"),
                    rs.getString("Status"),
                    rs.getString("Reason"),
                    rs.getString("Notes")
                };
                model.addRow(row);
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(EmployeeRecordviewer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
